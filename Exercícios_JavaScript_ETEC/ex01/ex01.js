//Escreva um programa que leia dois valores inteiros para as variáveis A e B. Em seguida, o programa deve efetuar a troca dos valores, de modo que a variável A passe a possuir o valor da variável B e a variável B passe a possuir o valor da variável A. Por fim, o programa deve apresentar os valores trocados.

var A = parseInt(prompt("Coloque um valor"));
var B = parseInt(prompt("Coloque outro valor"));
var n1 = A
var n2 = B
alert(`Seu primeiro valor agora tomou o lugar do segundo.`)
confirm(`O primeiro valor era ${A} e agora é ${n2}!`)
confirm(`O segundo valor era ${B} e agora é ${n1}!`)
